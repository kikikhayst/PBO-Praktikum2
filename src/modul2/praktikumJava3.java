package modul2;

public class praktikumJava3 {

    public static void main(String[] args) {
        float x = 19, y = 7;

        System.out.println("x = " + (int) x);
        System.out.println("y = " + (int) y);
        System.out.println("x + y = " + (int) (x + y));
        System.out.println("x - y = " + (int) (x - y));
        System.out.println("x * y = " + (int) (x * y));
        System.out.println("x / y = " + (x / y));
        System.out.println("x % y = " + (int) (x % y));
    }
}
